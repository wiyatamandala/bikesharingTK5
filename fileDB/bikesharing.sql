create schema bikesharing;

set search_path to public;

create table ACARA(id_acara varchar(10), judul varchar(100) NOT NULL, deskripsi text, tgl_mulai date NOT NULL, tgl_akhir date NOT NULL, is_free boolean NOT NULL, PRIMARY KEY(id_acara));

create table STASIUN(id_stasiun varchar(10), alamat text NOT NULL, lat real, long real, nama varchar(50) NOT NULL, PRIMARY KEY(id_stasiun));

create table ACARA_STASIUN(id_stasiun varchar(10), id_acara varchar(10), PRIMARY KEY(id_stasiun, id_acara), FOREIGN KEY(id_stasiun) REFERENCES STASIUN(id_stasiun), FOREIGN KEY(id_acara) REFERENCES ACARA(id_acara) ON UPDATE CASCADE ON DELETE CASCADE);

create table PERSON(ktp varchar(20), email varchar(50) unique NOT NULL, nama varchar(50) NOT NULL, alamat text, tgl_lahir date NOT NULL, no_telp varchar(20), PRIMARY KEY(ktp));

create table ANGGOTA(no_kartu varchar(10), saldo real, points int, ktp varchar(20) NOT NULL, PRIMARY KEY(no_kartu), FOREIGN KEY(ktp) REFERENCES PERSON(ktp) ON UPDATE CASCADE ON DELETE CASCADE);

create table SEPEDA(nomor varchar(10), merk varchar(10) NOT NULL, jenis varchar(50) NOT NULL, status boolean NOT NULL, id_stasiun varchar(10) NOT NULL, no_kartu_penyumbang varchar(20), PRIMARY KEY(nomor), FOREIGN KEY(id_stasiun) REFERENCES STASIUN(id_stasiun), FOREIGN KEY(no_kartu_penyumbang) REFERENCES ANGGOTA(no_kartu) ON UPDATE CASCADE ON DELETE CASCADE);

create table PETUGAS(ktp varchar(20), gaji real NOT NULL, PRIMARY KEY(ktp), FOREIGN KEY(ktp) REFERENCES PERSON(ktp) ON UPDATE CASCADE ON DELETE CASCADE);

create table PENUGASAN(ktp varchar(20), start_datetime timestamp, id_stasiun varchar(10), end_datetime timestamp NOT NULL, PRIMARY KEY(ktp, start_datetime, id_stasiun), FOREIGN KEY(ktp) REFERENCES PETUGAS(ktp), FOREIGN KEY(id_stasiun) REFERENCES STASIUN(id_stasiun) ON UPDATE CASCADE ON DELETE CASCADE);

create table PEMINJAMAN(no_kartu_anggota varchar(10), datetime_pinjam timestamp, nomor_sepeda varchar(10), id_stasiun varchar(10), datetime_kembali timestamp, biaya real, denda real, PRIMARY KEY(no_kartu_anggota, datetime_pinjam, nomor_sepeda, id_stasiun), FOREIGN KEY(nomor_sepeda) REFERENCES SEPEDA(nomor), FOREIGN KEY(id_stasiun) REFERENCES STASIUN(id_stasiun), FOREIGN KEY(no_kartu_anggota) REFERENCES ANGGOTA(no_kartu) ON UPDATE CASCADE ON DELETE CASCADE);

create table LAPORAN(id_laporan varchar(10), no_kartu_anggota varchar(10), datetime_pinjam timestamp, nomor_sepeda varchar(10), id_stasiun varchar(10), status varchar(20) NOT NULL, PRIMARY KEY(id_laporan, no_kartu_anggota, datetime_pinjam, nomor_sepeda, id_stasiun), FOREIGN KEY(no_kartu_anggota, datetime_pinjam, nomor_sepeda, id_stasiun) REFERENCES PEMINJAMAN(no_kartu_anggota, datetime_pinjam, nomor_sepeda, id_stasiun) ON UPDATE CASCADE ON DELETE CASCADE);

create table TRANSAKSI(no_kartu_anggota varchar(10), date_time timestamp, jenis varchar(20) NOT NULL, nominal real NOT NULL, PRIMARY KEY(no_kartu_anggota, date_time), FOREIGN KEY(no_kartu_anggota) REFERENCES ANGGOTA(no_kartu) ON UPDATE CASCADE ON DELETE CASCADE);

create table TRANSAKSI_KHUSUS_PEMINJAMAN(no_kartu_anggota varchar(10), date_time timestamp, no_kartu_peminjam varchar(10), datetime_pinjam timestamp, no_sepeda varchar(10), id_stasiun varchar(10), PRIMARY KEY(no_kartu_anggota, date_time), FOREIGN KEY(no_kartu_anggota, date_time) REFERENCES TRANSAKSI(no_kartu_anggota, date_time), FOREIGN KEY(no_kartu_peminjam, datetime_pinjam, no_sepeda, id_stasiun) REFERENCES PEMINJAMAN(no_kartu_anggota, datetime_pinjam, nomor_sepeda, id_stasiun) ON UPDATE CASCADE ON DELETE CASCADE);

create table VOUCHER(id_voucher varchar(10), nama varchar(255) NOT NULL, kategori varchar(255) NOT NULL, nilai_poin real NOT NULL, deskripsi text, no_kartu_anggota varchar(10), PRIMARY KEY(id_voucher), FOREIGN KEY(no_kartu_anggota) REFERENCES ANGGOTA(no_kartu) ON UPDATE CASCADE ON DELETE CASCADE);

create table ci_users (id int NOT NULL primary key, username varchar(50) NOT NULL, firstname varchar(30) NOT NULL,  lastname varchar(30) NOT NULL, email varchar(50) NOT NULL, mobile_no varchar(30) NOT NULL, password varchar(255) NOT NULL, is_admin int NOT NULL DEFAULT '0', last_ip varchar(30) NOT NULL, created_at date NOT NULL, updated_at date NOT NULL);


